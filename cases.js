//switch case is used to handle different selections.

let bankname="HDFC";
switch(bankname){
    case "sbi":
        console.log("Welcome to SBI");
        break;
        case "ICICI":
        console.log("Welcome to ICICI");
        break;
        case "Kotak":
        console.log("Welcome to Kotak");
        break;
        case "HDFC":
        console.log("Welcome to HDFC");
        break;
        case "Industrial":
        console.log("Welcome to Industrial");
        break;
       default:
        console.log("Incorrect bank selected");
        break;
}